    /**
     * When enter is pressed, move to next field
     * @param evt 
     */
    private void actOnEnter(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_actOnEnter

        // If enter is pressed
        if (evt.getKeyCode() == 10) {

            // Get name of field pressed
            String curName = ((JTextField) evt.getComponent()).getName();

            // If currently focused on num1
            if (curName.equals("num1")) {

                // Clear num2 
                ((JTextField) getComponentByName("num2")).setText("");

                // Move focus to num2
                ((JTextField) getComponentByName("num2")).requestFocus();
            }
            else
            {
                // If on num2, go to next operator
                ((JButton) getComponentByName("powOp")).requestFocus();
            }
        }
    }//GEN-LAST:event_actOnEnter

    /**
     * Retrieve a SimpCalc component by its name
     *
     * @param nameQuery
     * @return
     */
    public Component getComponentByName(String nameQuery) {
        // Return variable
        Component comp = null;

        // Get all components
        JRootPane jrp = (JRootPane) Entry.simpCalc.getComponents()[0];
        Container cp = (Container) jrp.getContentPane();
        JPanel jp = (JPanel) cp.getComponents()[0];
        Component[] parts = jp.getComponents();

        // Iterate over all parts
        for (Component curComp : parts) {
            // When name matches, save and stop
            if (nameQuery.equalsIgnoreCase(curComp.getName())) {
                comp = curComp;
                break;
            }
        }

        // Return component
        return comp;
    }

