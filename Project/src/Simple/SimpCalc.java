    /**
     * Process the two inputted numbers with a given operator
     *
     * @param op
     */
    private void calculate(char op) {

        try {
            // Get input
            String firstS = ((JTextField) getComponentByName("num1")).getText();
            double first = Double.parseDouble(firstS);
            String secondS = ((JTextField) getComponentByName("num2")).getText();
            double second = Double.parseDouble(secondS);

            // Compute with processor
            Processor proc = new Processor();
            String result = proc.evaluateArgs(first, second, op);
            ((JLabel) getComponentByName("result")).setText(result);

        } catch (NumberFormatException e) {

            // If any errors occur, result is 'Error'
            ((JLabel) getComponentByName("result")).setText("Error");

        }
    }

